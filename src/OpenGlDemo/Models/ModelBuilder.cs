        }

        public static Model ImportFromFile(string path, Vector3 position, Action bindAttributes)
        {
            var importer = new AssimpContext();
            Scene scene = importer.ImportFile(path, PostProcessSteps.GenerateNormals | PostProcessSteps.Triangulate);
            var vertices = new List<float>();
            var indices = new List<uint>();

            int indexOffset = 0;

            foreach (var mesh in scene.Meshes)
            {
                for (int i = 0; i < mesh.Vertices.Count; i++)
                {
                    if (mesh.HasNormals)
                    {
                        vertices.Add(mesh.Vertices[i].X);
                        vertices.Add(mesh.Vertices[i].Y);
                        vertices.Add(mesh.Vertices[i].Z);
                        vertices.Add(mesh.Normals[i].X);
                        vertices.Add(mesh.Normals[i].Y);
                        vertices.Add(mesh.Normals[i].Z);
                    }
                }

                foreach (var face in mesh.Faces)
                {
                    foreach (var index in face.Indices)
                    {
                        indices.Add((uint)(index + indexOffset));
                    }
                }

                indexOffset += mesh.VertexCount;
            }

            return new Model(vertices.ToArray(), indices.ToArray(), position, bindAttributes);
        }
