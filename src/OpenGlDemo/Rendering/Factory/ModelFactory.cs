            0.5f, -0.5f, 0.0f,
            0.0f,  0.5f, 0.0f
        };
        private static float[] _rectangleVertexes => new float[]
        {
            0.5f, 0.5f, 0.0f,
            0.5f, -0.5f, 0.0f,
            -0.5f, -0.5f, 0.0f,
            -0.5f, 0.5f, 0.0f
        };
