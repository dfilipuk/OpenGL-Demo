        {
            0, 1, 3,
            1, 2, 3
        };

        public static Model CreateTriangle(Vector3 position, Action bindAttributes)
        {
            return new Model(_triangleVertexes, 3, position, bindAttributes);
        }

        public static Model CreateRectangle(Vector3 position, Action bindAttributes)
        {
            return new Model(_rectangleVertexes, _rectangleIndices, 6, position, bindAttributes);
        }

        public static Model CreateCube(Vector3 position, Action bindAttributes)
        {
            return new Model(_cubeVertexes, 36, position, bindAttributes);
        }
